-- calc size of non-array tables
function size(tbl)
	acc = 0
	for x in pairs(tbl) do
		acc += 1
	end
	return acc
end

function concat_with(stringlist, separator)
	local result
	for v in all(stringlist) do
		if not result then
			result = v
		else
			result = result .. separator .. v
		end
	end
	return result
end

function shift_letter(ltr, delta)
	local i = ord(ltr) - ord("a")
	i += delta
	i = (i + 26) % 26
	return chr(i + ord("a"))
end

